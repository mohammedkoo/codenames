function generateBoard(packName = "standard") {
  const wordsList = wordPacks[packName] || wordPacks.standard;
  
  // Shuffle words
  const shuffled = [...wordsList].sort(() => Math.random() - 0.5);
  
  // Pick 27 words
  const selectedWords = shuffled.slice(0, 27);
  
  // Create card types distribution: 9 red, 9 blue, 8 neutral, 1 killer
  const cardTypes = [
    ...Array(9).fill("red"),
    ...Array(9).fill("blue"),
    ...Array(8).fill("neutral"),
    "killer"
  ].sort(() => Math.random() - 0.5);

  return selectedWords.map((word, index) => {
    return {
      text: word,
      type: cardTypes[index],
      revealed: false
    };
  });
}


// Fisher-Yates (Knuth) Shuffle Algorithm (unbiased)
function shuffle(array) {
  for (let i = array.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    const temp = array[i];
    array[i] = array[j];
    array[j] = temp;
  }
  return array;
}

function generateBoard(packName = "standard") {
  const wordsList = wordPacks[packName] || wordPacks.standard;
  
  // Clone and Shuffle words using Fisher-Yates
  const shuffledWords = shuffle([...wordsList]);
  
  // Pick exactly 24 words for the 6x4 layout
  const selectedWords = shuffledWords.slice(0, 24);
  
  // Create card types distribution for 24 cards: 8 red, 8 blue, 7 neutral, 1 killer
  const cardTypes = shuffle([
    ...Array(8).fill("red"),
    ...Array(8).fill("blue"),
    ...Array(7).fill("neutral"),
    "killer"
  ]);

  return selectedWords.map((word, index) => {
    return {
      text: word,
      type: cardTypes[index],
      revealed: false
    };
  });
}

